import 'src/shared/theme/globals.scss';

import clsx from 'clsx';
import type { Metadata } from 'next';
import { NextIntlClientProvider } from 'next-intl';
import { getMessages } from 'next-intl/server';

import { AppProviders } from 'src/shared/providers';
import { iranSansXFont } from 'src/shared/theme';
import { getLocaleDir, getLocaleLang } from 'src/shared/utils';

export const metadata: Metadata = {
  title: 'Resume Builder',
  description: 'Generated by Blufie team',
};

export default async function RootLayout({ children, params }: LayoutProps) {
  // Providing all messages to the client
  // side is the easiest way to get started
  const messages = await getMessages();

  return (
    <html
      dir={getLocaleDir(params.locale)}
      lang={getLocaleLang(params.locale)}
      className={clsx(iranSansXFont.className, iranSansXFont.variable)}
    >
      <body>
        <NextIntlClientProvider messages={messages}>
          <AppProviders>{children}</AppProviders>
        </NextIntlClientProvider>
      </body>
    </html>
  );
}
